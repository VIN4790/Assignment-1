# 1.TWO SUM PROBLEM
def chekpair(A,size,x):
    for i in range(0,size-1):
        for j in range(1,size):
           if A[i]+A[j]==x:
                return([i,j])
            
A=[1,3,15,6,5]
size=len(A)
x=7
print(chekpair(A,size,x))

